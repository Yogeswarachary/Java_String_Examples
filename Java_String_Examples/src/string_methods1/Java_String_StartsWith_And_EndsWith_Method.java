package string_methods1;

public class Java_String_StartsWith_And_EndsWith_Method {
	public static void main(String[] args) {
		String s="Mahindra Singh Dhoni";
		System.out.println(s.startsWith("Ma"));
		System.out.println(s.endsWith("ni"));
		System.out.println(s.startsWith("Ka"));
		System.out.println(s.endsWith("Lo"));
	}
}
